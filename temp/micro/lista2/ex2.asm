    #include <p16f877a.inc>
    __config b'11110100011000'
    org 0x0000
    
    CLRF STATUS

    CLRF PORTB
    CLRF PORTC

    BANKSEL TRISB
    MOVLW 0XFF
    MOVWF TRISB

    BANKSEL TRISC
    MOVLW 0X00
    MOVWF TRISC

    BANKSEL PORTC

START
    BTFSC PORTB,RB1;SKIP IF ZERO
    BCF PORTC,RC1
    BTFSS PORTB,RB1;SKIP IF ONE
    BSF PORTC,RC1
    GOTO START

    end
