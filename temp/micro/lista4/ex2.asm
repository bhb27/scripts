    #INCLUDE <P16F877A.INC>
    __CONFIG B'11110100011000'
    ORG 0X0000
    
    CLRF STATUS

    CLRF PORTC

    BANKSEL TRISC
    MOVLW 0X00
    MOVWF TRISC

    BANKSEL PORTC
    MOVLW 0x00
    MOVWF PORTC

START; OSCILOSCOPIO ON DC
    ; f(n) = n; /
    INCFSZ PORTC,1
    BTFSS  PORTC,0XFF;PREVE 00 AFTER FF
    GOTO START

START2
    ; f(n) = -n; \;
    DECFSZ PORTC,1
    GOTO START2
    GOTO START


    END
