    #INCLUDE <P16F877A.INC>
    __CONFIG B'11110100011000'
    ORG 0X0000
    
    CLRF STATUS

    CLRF PORTC

    BANKSEL TRISC
    MOVLW 0X00
    MOVWF TRISC

    BANKSEL PORTC

    CBLOCK
    D0
    D1
    ENDC

START; OSCILOSCOPIO EM DC
    MOVLW 0xFF
    MOVWF PORTC
    CALL  DELAY

    MOVLW 0x0
    MOVWF PORTC
    CALL  DELAY

    GOTO START

DELAY;127*2 DELAY SAME FOR UP AND DOWN
    MOVLW    0X7F
    MOVWF    D0
    MOVLW    0X01
    MOVWF    D1
DELAY_0
    DECFSZ   D0, F
    GOTO     $+2
    DECFSZ   D1, F
    GOTO     DELAY_0
    RETURN


    END
