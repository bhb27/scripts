    #INCLUDE <P16F877A.INC>
    __CONFIG B'11110100011000'
    ORG 0X0000
    
    CLRF STATUS
    CLRF PORTC

    BANKSEL TRISC
    MOVLW 0X00
    MOVWF TRISC

    CBLOCK
    D0
    D1
    ENDC

    BANKSEL PORTC

START
    BSF PORTC,RC0
    CALL DELAY_30
    CALL DELAY_30
    BCF PORTC,RC0

    BSF PORTC,RC3
    CALL DELAY_10
    BCF  PORTC,RC3

    BSF  PORTC,RC6
    CALL DELAY_30
    CALL DELAY_30
    CALL DELAY_30
    BCF  PORTC,RC6
    GOTO START

DELAY_30
    ;30 SECONDS DELAY
    ;5998 CYCLES
    MOVLW    0xAF
    MOVWF    D0
    MOVLW    0x05
    MOVWF    D1
DELAY_0
    DECFSZ   D0, F
    GOTO     $+2
    DECFSZ   D1, F
    GOTO     DELAY_0
    ;2 CYCLES
    RETURN

DELAY_10
    ;10 SECONDS DELAY
    ;1998 CYCLES
    MOVLW    0x8F
    MOVWF    D0
    MOVLW    0x02
    MOVWF    D1
DELAY_2
    DECFSZ   D0, F
    GOTO     $+2
    DECFSZ   D1, F
    GOTO     DELAY_2
    ;2 CYCLES
    RETURN

    END
